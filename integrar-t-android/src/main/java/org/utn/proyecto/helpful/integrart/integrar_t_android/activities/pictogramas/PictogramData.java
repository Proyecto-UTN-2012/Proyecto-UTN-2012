package org.utn.proyecto.helpful.integrart.integrar_t_android.activities.pictogramas;

public class PictogramData {
	private String name;
	private int level;
	
	public PictogramData(String name, int level){
		this.name = name;
		this.level = level;
	}
	
	public String getName() {
		return name;
	}

	public int getLevel() {
		return level;
	}
}
